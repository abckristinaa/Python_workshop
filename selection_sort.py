'''Вариант реализации сортировки массива а выбором (по возрастанию)'''


a = [1, 5, 5, 8, 6, 45, 19, 0]

d = 1
while d < len(a):
    i = 0
    m = i
    while i <= len(a) - 1 - d:
        if a[m] < a[i + 1]:
            m = i + 1
        i += 1
    a[len(a) - d], a[m] = a[m], a[len(a) - d]
    d += 1
